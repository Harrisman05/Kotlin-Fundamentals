/* Collections and Iteration

Collection data types includes arrays, lists and map. Used to group values
together.
 */

fun main() {

    // Defining an array

    val interestingThings = arrayOf("Kotlin", "Harley", "Films")

    println(interestingThings.size) // length of array
    println(interestingThings[0]) // access first element
    println(interestingThings.get(0)) // also access 1st element



}